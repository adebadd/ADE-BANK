    public void addCustomerToJtable() {
        ArrayList<Customer> customers = Customer.customers;
        DefaultTableModel customerModel = (DefaultTableModel) this.jTable1.getModel();
        ArrayList<String[]> data = new ArrayList<>();

        customerModel.getDataVector().clear();

        for (int i = 0; i < customers.size(); i++) {
            Customer currentcustomer = customers.get(i);

            data.add(new String[]{currentcustomer.getCustomerFirst(), currentcustomer.getCustomerLast(), currentcustomer.getCustomerID(), currentcustomer.getCustomerPPSN(), currentcustomer.getCustomerDOB(), currentcustomer.getAccountNum()});

        }
        String[][] one = data.toArray(new String[0][0]);
        customerModel.setDataVector(one, new String[]{"Customer First Name", "Customer Last Name", "Customer ID", "Customer PPSN", "Customer DOB", "Customer Account Number"});

        this.jPanel1.updateUI();
        customerModel.fireTableDataChanged();
        jTable1.setEnabled(false);
    }
    
     public void addCustomerTransactionsToJtable() {
        ArrayList<AccountTransactions> transactions = AccountTransactions.transactions;
        DefaultTableModel transactionsModel = (DefaultTableModel) this.jTable2.getModel();
        ArrayList<String[]> data = new ArrayList<>();

        transactionsModel.getDataVector().clear();

        for (int i = 0; i < transactions.size(); i++) {
            AccountTransactions currenttransactions = transactions.get(i);

            data.add(new String[]{currenttransactions.getCustomerAccount(), Double.toString(currenttransactions.getCustomerBalance()), currenttransactions.getTransactionDate(), currenttransactions.getTransactionType(),Double.toString(currenttransactions.getTransactionAmount()) });

        }
        String[][] one = data.toArray(new String[0][0]);
        transactionsModel.setDataVector(one, new String[]{"Customer First Name", "Customer Last Name", "Customer ID", "Customer PPSN", "Customer DOB", "Customer Account Number"});

        this.jPanel1.updateUI();
        transactionsModel.fireTableDataChanged();
        jTable2.setEnabled(false);
    }



